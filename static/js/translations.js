function changeUILanguage(lang) {
    console.log("Changing UI language to:", lang);
    if (!uiTranslations[lang]) return;
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (uiTranslations[lang][key]) {
            if (el.tagName === 'TITLE') {
                document.title = uiTranslations[lang][key];
            } else {
                el.innerHTML = uiTranslations[lang][key];
            }
        }
    });
    
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (uiTranslations[lang][key]) {
            el.placeholder = uiTranslations[lang][key];
        }
    });

    const uiSelectors = document.querySelectorAll('select#ui-language, select#mobile-ui-language');
    uiSelectors.forEach(select => {
        if(select.value !== lang) {
            select.value = lang;
        }
    });
    
    window.currentUILang = lang;
    localStorage.setItem('uiLanguage', lang);
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
}

document.addEventListener('DOMContentLoaded', () => {
    const savedUILang = localStorage.getItem('uiLanguage') || 'en';
    changeUILanguage(savedUILang);
});
